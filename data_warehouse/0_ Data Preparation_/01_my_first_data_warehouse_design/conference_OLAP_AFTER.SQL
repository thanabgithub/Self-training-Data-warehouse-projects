
CREATE TABLE DIM_PRESENTATION(
    PRESENTATION_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    ROOM_ID INTEGER NOT NULL,
    FLOOR_NUMBER INTEGER NOT NULL,
    SEAT_CAPACITY INTEGER NOT NULL,
    START_TIME TIME NOT NULL,
    END_TIME TIME NOT NULL
);

CREATE TABLE DIM_COMPANY(
    COMPANY_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    NAME VARCHAR(20) NOT NULL
);

CREATE TABLE DIM_VIP(
    VIP_FLAG_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    VIP BOOLEAN DEFAULT(FALSE)
);

CREATE TABLE FACT_ATTENDDEE(
    ATTENDDEE_ID INTEGER PRIMARY KEY AUTOINCREMENT,
    TICKET_ID INTEGER NOT NULL,
    FIRST_NAME VARCHAR(20) NOT NULL,
    LAST_NAME VARCHAR(20) NOT NULL,
    PHONE VARCHAR(10) NOT NULL,
    EMAIL VARCHAR(20) NOT NULL,
    PRESENTATION_ID INTEGER,
    COMPANY_ID INTEGER,
    VIP_FLAG_ID INTEGER,
    
    FOREIGN KEY (PRESENTATION_ID) REFERENCES DIM_PRESENTATION(PRESENTATION_ID),    
    FOREIGN KEY (COMPANY_ID) REFERENCES DIM_COMPANY(COMPANY_ID)    
    FOREIGN KEY (VIP_FLAG_ID) REFERENCES DIM_VIP(VIP_FLAG_ID)    
);